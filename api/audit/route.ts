/* ============================================================
   المعالج — يستقبل الرابط، ينتظر، ثم يرجّع الـ Mock
   ============================================================ */
export async function POST(req: NextRequest) {
  // نقرأ الرابط فقط لعرضه في التقرير (بدون أي سحب أو تحقق صارم)
  const body = await req.json().catch(() => ({}));
  const rawUrl = typeof body?.url === "string" ? body.url.trim() : "";
  const normalized = rawUrl
    ? /^https?:\/\//i.test(rawUrl)
      ? rawUrl
      : `https://${rawUrl}`
    : "https://demo-store.com";

  // محاكاة زمن المعالجة لإظهار شاشة التحميل (Skeleton)
  await sleep(DEMO_DELAY_MS);

  return NextResponse.json(
    {
      ok: true,
      url: normalized,
      scrapedTitle: safeHost(normalized),
      demo: true, // علامة توضّح أن النتيجة تجريبية
      ...MOCK_REPORT,
    },
    { status: 200 }
  );
}

/* ============================================================
   مساعدات
   ============================================================ */
function sleep(ms: number) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

function safeHost(url: string) {
  try {
    return new URL(url).host;
  } catch {
    return url;
  }
}
